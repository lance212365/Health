  <p>Dementia is a progressive disease of the brain. There is decline in cognitive and intellectual function such as memory, comprehension, learning capacity, the ability to think and calculate, as well as language and judgment. Patients gradually deteriorate with impaired functioning in daily activities, some also with emotional and behavioural problems. According to a population-based study conducted jointly by the Elderly Health Service, Department of Health and the Department of Psychiatry, the Chinese University of Hong Kong in 2005-2006, the prevalence of dementia among community elders aged 70 or above was 9.3%, with 15.3% for female and 8.9% for male.</p>
  <h2>Causes of dementia </h2>
  <ul>
    <li>Alzheimer's Disease: cause unknown, likely to be hereditary in nature, the most   common type (about 65%) </li>
    <li>Vascular Dementia: due to multiple minor strokes, affects about 30% of   elders with dementia </li>
    <li>Other causes: e.g. brain tumour, head injury, vitamin B 12 deficiency, drug   and alcohol abuse, hypothyroidism and depression </li>
  </ul>
  
  
  <h2>Stages of dementia and common presentations </h2>
  <ul>
    <li>Early stage - memory loss with decreased ability to think </li>
    <li>Intermediate stage - easily get lost, mood changes and behavioral problems,   need assistance with daily activities </li>
    <li>Late stage - language impairment, cannot recognize relatives, may become   incontinent and totally dependent </li>
    </ul>
  
  <h2>Management</h3>
  <ul>
    <li>Seek early medical advice and treatment if there is suspicion of dementia. </li>
    <li>Some drugs may improve memory and cognitive functions of patient with early   Alzheimer's Disease. Non-cognitive symptoms such as depression, psychotic   features and sleep problems may benefit from drug treatment as well. However,   there is no cure for dementia. </li>
    <li>Other treatment includes behavioral therapy, programmes to improve memory   and cognitive function. Education and counseling to patients and carers are also   important. </li>
    <li>Carers of demented patient can attend health talks, support groups etc. to   acquire more caring skills and stress management techniques. </li>
    </ul>
  <h2>Prevention</h3>
  <ul>
    <li>Prevention of Alzheimer's Disease is not yet known. </li>
    <li>Multi-infarct dementia may be prevented by adopting a healthy lifestyle   which includes a balanced diet, regular exercise, no smoking and no excessive   drinking. Proper control of hypertension and diabetes mellitus is also   essential. </li>
  </ul>
